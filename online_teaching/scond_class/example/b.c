#include <stdio.h>

int main() {
    printf("=== 特殊字符转义输出示例 ===\n\n");
    
    // 打印反斜杠
    printf("1. 反斜杠: \\ \n");
    
    // 打印双引号
    printf("2. 双引号: \"Hello World\" \n");
    
    // 打印单引号
    printf("3. 单引号: \'A\' \n");
    
    // 打印换行符
    printf("4. 换行符: 第一行\\n第二行\n");
    
    // 打印制表符
    printf("5. 制表符: 姓名\\t年龄\\t成绩\n");

    // 打印百分号
    printf("6. 百分号: 100%%");
    return 0;
}